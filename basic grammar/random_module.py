import random

# randint : 두 수 사이의 어떤 랜덤한 정수를 리턴하는 함수
# randint(a, b) : a와 b 사이의 정수를 랜덤으로 리턴
print("-- randint method --")
print(random.randint(1, 10))
print(random.randint(1, 10))
print(random.randint(1, 10))


# uniform : 두 수 사이의 랜덤한 소수를 리턴하는 함수
# uniform(a, b) : a와 b 사이의 소수를 랜덤으로 리턴
print("-- uniform method --")
print(random.uniform(0, 1))
print(random.uniform(0, 1))
print(random.uniform(0, 1))
